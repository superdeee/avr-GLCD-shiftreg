void drawBar(uint8_t y)
{
    Send16(0x0440);
    Send16(0x0840);
    Send16(0x04B8 + y);
    Send16(0x08B8 + y);
    for(int i=0;i<64;i++)
    {
        Send16(0x24FF);
    }
    for(int i=64;i<128;i++)
    {
        Send16(0x28FF);
    }
}

void drawDot(uint8_t x, uint8_t y)
{
    if(x<64)
    {
        Send16(0x0440 + x); //01XX XXXX
        Send16(0x04B8 + (y/8));
        uint8_t z = 1;
        for(int i=0; i<(y%8);i++) z*=2;
        Send16(0x2400 + z);
    }
    else
    {
        x-=64;
        Send16(0x0840 + x); //01XX XXXX
        Send16(0x08B8 + (y/8));
        uint8_t z = 1;
        for(int i=0; i<(y%8);i++) z*=2;
        Send16(0x2800 + z);
    }
    pos_x=x+1;
    pos_y=y/8;
}

