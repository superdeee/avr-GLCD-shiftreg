  /// E|x|RS|R/W|CS1|CS2|x|x|D7-D0
  /// 0x04XX inst CS1   0x08XX inst CS2
  /// 0x24XX dane CS1   0x28XX dane CS2

void Send16(unsigned int data)
{
    //SET_ENB();
    CLR_ENB();
    for(int i=0;i<16;i++)
    {
      if(data & 0x01) SET_DATA();
      else CLR_DATA();

      SET_CLK();
      asm("nop");
      CLR_CLK();

      data = data >> 1;
    }

    SET_CLK();
    asm("nop");
    CLR_CLK();

    SET_ENB();
    CLR_ENB();
}

void CleanLCD1()
{
  int a = 0x04B8;
  Send16(a); // X=0
  Send16(0x0440); // Y=0

  for(int i=0;i<8;i++)
  {
    for(int j=0;j<64;j++)
    {
      Send16(0xA400);
    }
    Send16(++a);
  }
  Send16(0x0440);
  pos_x = 0;
  Send16(0x04B8);
  pos_y = 0;
}

void CleanLCD2()
{
  int a = 0x08B8;
  Send16(a); // X=0
  Send16(0x0840); // Y=0

  for(int i=0;i<8;i++)
  {
    for(int j=0;j<64;j++)
    {
      Send16(0xA800);
    }
    Send16(++a);
  }
  Send16(0x0840);
  pos_x = 0;
  Send16(0x08B8);
  pos_y = 0;
}

void GoToXY(uint8_t x, uint8_t y)
{
  if(x<64)
  {
    Send16(0x0440 + x);
    Send16(0x0840);
  }
  else
  {
    Send16(0x0840 + (x-64));
  }
    pos_x = x;
    Send16(0x04B8 + (y/8));
    Send16(0x08B8 + (y/8));
    pos_y = y/8;
}

void InitLCD()
{
  Send16(0x043F); //wlacz
  Send16(0x083F); //wlacz
  Send16(0x04C0); // Z=0
  Send16(0x08C0); // Z=0
  CleanLCD1();
  CleanLCD2();
}

void Write(char text[], uint8_t textSize, bool neg)
{
    for(int i=0;i<textSize;i++)
    {
        drawLetter(text[i] - 32, neg);
    }
    if(neg == true)
    {
    if(pos_x<64) Send16(0x24FF);
    else Send16(0x28FF);
    }
    pos_x++;
}

